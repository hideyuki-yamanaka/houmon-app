const HIRAGANA_REGEX = /^[ぁ-ゖー\u3000\s]+$/;
const KATAKANA_REGEX = /^[ァ-ヺー\u3000\s]+$/;

/** カタカナをひらがなに変換 */
function katakanaToHiragana(str: string): string {
  return str.replace(/[ァ-ヶ]/g, (ch) =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60)
  );
}

/**
 * 名前から読み仮名を超ざっくり推測する。
 * 必ず末尾に「（仮）」を付ける。
 */
export function guessKana(name: string | undefined | null): string {
  if (!name) return '';
  const trimmed = name.replace(/\s+/g, '').trim();
  if (!trimmed) return '';

  // 既にひらがな → そのまま（仮）
  if (HIRAGANA_REGEX.test(trimmed)) return `${trimmed}（仮）`;
  // カタカナ → ひらがなに寄せて（仮）
  if (KATAKANA_REGEX.test(trimmed)) return `${katakanaToHiragana(trimmed)}（仮）`;

  // 2文字連結の熟字訓を先にマッチさせる
  let result = '';
  let i = 0;
  while (i < trimmed.length) {
    const two = trimmed.slice(i, i + 2);
    if (two.length === 2 && KANJI_READINGS[two]) {
      result += KANJI_READINGS[two];
      i += 2;
      continue;
    }
    const one = trimmed[i];
    if (KANJI_READINGS[one]) {
      result += KANJI_READINGS[one];
    } else if (HIRAGANA_REGEX.test(one) || KATAKANA_REGEX.test(one)) {
      result += KATAKANA_REGEX.test(one) ? katakanaToHiragana(one) : one;
    } else {
      // 読めない字はとりあえず「？」
      result += '？';
    }
    i += 1;
  }

  // 全然読めんかったら原文返し
  if (!result || /^[？]+$/.test(result)) return `${trimmed}（仮）`;
  return `${result}（仮）`;
}
